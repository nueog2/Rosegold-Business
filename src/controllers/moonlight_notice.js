const message = require("../../config/message");
const Moonlight_Notice = require("../models/moonlight_notice").Moonlight_Notice;

function createMoonlightNotice(req, res) {
  if (req.body.content == null || req.body.hotel_id == null) {
    return res
      .status(message["400_BAD_REQUEST"].status)
      .send(
        message.issueMessage(message["400_BAD_REQUEST"], "SEND_ALL_PARAMETERS")
      );
  }
  const moonlight_notice = new Moonlight_Notice();
  moonlight_notice
    .create(req.body.content, req.body.hotel_id)
    .then((response) => {
      return res.status(response.status).send(response);
    })
    .catch((error) => {
      console.log(error);
      if (!error.status)
        return res
          .status(message["500_SERVER_INTERNAL_ERROR"].status)
          .send(
            message.issueMessage(
              message["500_SERVER_INTERNAL_ERROR"],
              "UNDEFINED_ERROR"
            )
          );
      else return res.status(error.status).send(error);
    });
}

